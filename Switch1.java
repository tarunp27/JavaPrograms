public class Switch1{
    public static void main(String[] args){
        int number = 100;

        switch(number){

            case 30:
            System.out.println("Economy");
            break;

            case 50:
            System.out.println("Sport");
            break;

            case 100:
            System.out.println("Overspeed");
            break;
    
            default :
            System.out.println("Khatham");
            break;
        }
    }
}